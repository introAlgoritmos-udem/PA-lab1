"""
Este programa calcula el area de un triangulo
@author: pensamiento-algoritmico
"""

# Entrada de las dimensiones
base = float(input("Digite la base: "))
heigh = float(input("Digite la altura: "))

# Calculo del area
area = (base*heigh)/2

# Despliegue del area
print("El area del triangulo es", area)

